    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if (cb_tipoDocs.getSelectedItem().equals("-- Selecione o Documento --")) {
            JOptionPane.showMessageDialog(this, "Selecione o Documento", "Alerta", JOptionPane.WARNING_MESSAGE);
        } else {
            Funcionario c = new Funcionario();
            FuncionarioDAO dao = new FuncionarioDAO();
            if (tb_clientes.getSelectedRow() != -1) {
                c.setId((int) tb_clientes.getValueAt(tb_clientes.getSelectedRow(), 0));
            }
            c.setTipoDocumento((String) cb_tipoDocs.getSelectedItem());
            if (rb_fem.isSelected()) {
                c.setSexo("Femenino");
            }
            c.setNome(tf_nome.getText());
            c.setMorada(tf_morada.getText());
            c.setNumeroDocumento(tf_nrDocs.getText());
            c.setTelefone(tf_telefone.getText());
            c.setCargo(tf_cargo.getText());
            dao.save(c);
            cb_tipoDocs.setSelectedItem("-- Selecione o Documento --");
            tf_morada.setText("");
            tf_nome.setText("");
            tf_nrDocs.setText("");
            tf_telefone.setText("");
            tf_cargo.setText("");

            readTable();
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deletarActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente Apagar o Cliente ?");
        if (opcao == 0) {
            if (tb_clientes.getSelectedRow() != -1) {
                Funcionario c = new Funcionario();
                FuncionarioDAO dao = new FuncionarioDAO();
                c.setId((int) tb_clientes.getValueAt(tb_clientes.getSelectedRow(), 0));
                dao.delete(c.getId());

                cb_tipoDocs.setSelectedItem("-- Selecione o Documento --");
                tf_morada.setText("");
                tf_nome.setText("");
                tf_nrDocs.setText("");
                tf_telefone.setText("");
                tf_cargo.setText("");

                readTable();
            }
        }
    }//GEN-LAST:event_btn_deletarActionPerformed

