    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tb_clientes.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO cdao = new FuncionarioDAO();

        for (Funcionario c : cdao.listAll()) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getTipoDocumento(),
                c.getNumeroDocumento(),
                c.getSexo(),
                c.getTelefone(),
                c.getMorada(),
                c.getCargo()
            });
        }
    }

