    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tb_lista.getModel();
        modelo.setNumRows(0);
        CriancaDAO udao = new CriancaDAO();

        for (Crianca c : udao.listAll()) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getHoraEntrada(),
                c.getHoraSaida()
            });
        }
    }

