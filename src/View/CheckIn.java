    private void btn_saiuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saiuActionPerformed
        Calendar cal = Calendar.getInstance(); //obter o calendario do sistema
        int hora = cal.get(Calendar.HOUR_OF_DAY);
        int minuto = cal.get(Calendar.MINUTE);
        if (tb_lista.getSelectedRow() != -1) {
            Crianca c = new Crianca();
            CriancaDAO dao = new CriancaDAO();
            c.setId((int) tb_lista.getValueAt(tb_lista.getSelectedRow(), 0));
            c.setHoraSaida(hora + "h:" + minuto + "m");
            dao.save(c);
            readTable();
        }
    }//GEN-LAST:event_btn_saiuActionPerformed

